      subroutine vswirlXYZ(x,y,u,v,w)
      implicit none
      double precision x,y,vel(3),u,v,w,r,eps, factor
      integer N
      parameter (eps=1.d-9, factor = 15.d0/13.261653269312280d0)

c     x,y come in as MKS.  Convert to mm.  Result already in MKS
      r = SQRT( x*x + y*y ) * 1000
      call vswirl(r,r,vel,N)

      vel = vel * factor

      if (r.lt.eps) then
         u = 0.d0
         v = 0.d0
      else
         u = 1000*(x*vel(1) - y*vel(2))/r
         v = 1000*(y*vel(1) + x*vel(2))/r
      endif
      w = vel(3)

      end


